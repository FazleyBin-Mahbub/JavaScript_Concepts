var num=9.567
// to find absolute number 
console.log(Math.abs(num))

// to find the integer value 
console.log(Math.floor(num))

// to find the next number of the input number 
console.log(Math.ceil(num))
// to find the nearest integer number
console.log(Math.round(num))
// to find the maximum number between two or multiple numbers 
console.log(Math.max(400,200))
// to find the minimum number between two or multiple numbers 
console.log(Math.min(200,120))
// to find the value of numbers power
console.log(Math.pow(2,3))

// to find the square root number 
console.log(Math.sqrt(36))
 
// to find modulus of these two numbers 
console.log(17%5);